let provider, signer, userAddress;
let token, nft;
let decimals = 18;

function setStatus(msg) {
  statusLabel.textContent = msg;
}

async function updateTokenInfo() {
  const [name, symbol, bal] = await Promise.all([
    token.name(),
    token.symbol(),
    token.balanceOf(userAddress)
  ]);

  tokenMetaSpan.textContent = `${name} (${symbol})`;
  tokenBalanceSpan.textContent = ethers.formatUnits(bal, decimals);
}

connectBtn.onclick = async () => {
  try {
    if (!window.ethereum) {
      alert("MetaMask not found");
      return;
    }

    provider = new ethers.BrowserProvider(window.ethereum);
    await provider.send("eth_requestAccounts", []);

    signer = await provider.getSigner();
    userAddress = await signer.getAddress();

    accountSpan.textContent = userAddress;

    token = new ethers.Contract(TOKEN_ADDRESS, ERC20_ABI, signer);
    nft = new ethers.Contract(NFT_ADDRESS, ERC721_ABI, signer);

    decimals = await token.decimals();

    await updateTokenInfo();
    setStatus("Connected!");

  } catch (e) {
    console.error(e);
    setStatus("Wallet connection failed.");
  }
};

transferBtn.onclick = async () => {
  try {
    if (!token) {
      setStatus("Connect wallet first.");
      return;
    }

    const to = toAddressInput.value.trim();
    const amount = amountInput.value.trim();

    if (!ethers.isAddress(to)) {
      setStatus("Invalid recipient address.");
      return;
    }
    if (!amount || Number(amount) <= 0) {
      setStatus("Enter a valid amount.");
      return;
    }

    const parsed = ethers.parseUnits(amount, decimals);

    setStatus("Transaction pending... Confirm in MetaMask.");

    const tx = await token.transfer(to, parsed);

    setStatus(`Tx sent: ${tx.hash}`);

    const receipt = await tx.wait();

    setStatus(`Transfer confirmed âœ… Tx: ${receipt.hash}`);

    await updateTokenInfo();

  } catch (e) {
    console.error(e);
    if (e?.code === 4001) setStatus("Transaction rejected in MetaMask.");
    else setStatus("Transfer failed. Check console.");
  }
};

loadNftsBtn.onclick = async () => {
  try {
    if (!nft) {
      setStatus("Connect wallet first.");
      return;
    }

    nftListDiv.innerHTML = "";
    setStatus("Loading NFTs...");

    const bal = await nft.balanceOf(userAddress);
    const count = Number(bal);

    if (count === 0) {
      setStatus("You have 0 NFTs.");
      return;
    }

    const MAX_SCAN = 50;
    let found = 0;

    for (let tokenId = 0; tokenId < MAX_SCAN && found < count; tokenId++) {
      try {
        const owner = await nft.ownerOf(tokenId);
        if (owner.toLowerCase() !== userAddress.toLowerCase()) continue;

        const uri = await nft.tokenURI(tokenId);

        const div = document.createElement("div");
        div.className = "item";
        div.textContent = `Token ID: ${tokenId}, URI: ${uri}`;
        nftListDiv.appendChild(div);

        found++;
      } catch (_) {
      }
    }

    setStatus(`Loaded ${found} NFT(s).`);

  } catch (e) {
    console.error(e);
    setStatus("Failed to load NFTs.");
  }
};
